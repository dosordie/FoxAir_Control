def cloud_hint(code: str) -> dict[str, object]:
    return WARMLINK_CLOUD_CODE_HINTS.get(str(code), {})


def cloud_modbus_register(code: str) -> int | None:
    reg = cloud_hint(code).get("modbus_register")
    try:
        return int(reg) if reg not in (None, "") else None
    except Exception:
        return None


def code_display_name(code: str) -> str:
    hint = cloud_hint(code)
    return str(hint.get("name") or code)


def code_unit(code: str) -> str:
    return str(cloud_hint(code).get("unit") or "")


def code_confidence(code: str) -> str:
    return str(cloud_hint(code).get("confidence") or "")
